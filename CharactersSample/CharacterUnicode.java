package CharactersSample;

public class CharacterUnicode {
    public static void main(String[] args) {
        char letter = 65;  // 65 corresponds to 'A'
        char digit = 53;   // 53 corresponds to '5'

        System.out.println("letter is: " + letter);  // Outputs: letter is: A
        System.out.println("digit is: " + digit);    // Outputs: digit is: 5
    }
}
